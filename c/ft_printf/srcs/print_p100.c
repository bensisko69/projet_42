#include "../includes/ft_printf.h"

int		print_p100(va_list list, int nbr)
{
	(void)list;
	int	b;

	b = nbr;
	while (nbr - 1 > 0)
	{
		write(1, " ", 1);
		nbr--;
	}
	write(1,"%", 1);
	return (b + 1);	
}