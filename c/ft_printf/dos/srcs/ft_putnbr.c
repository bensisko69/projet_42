#include "libft.h"

int	ft_putnbr(int n, int l)
{
	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return (11);
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
		l++;
	}
	if (n >= 10)
	{
		l++;
		l = ft_putnbr(n / 10, l);
		l = ft_putnbr(n % 10, l);
	}
	else
		ft_putchar(48 + n);
	return (l);
}
