#include "push_swap.h"

void		rra(t_list **l_a, char *func)
{
	(*l_a)->previous->fst = TRUE;
	(*l_a)->fst = FALSE;
	(*l_a) = (*l_a)->previous;
	write(1, func, 4);
}
