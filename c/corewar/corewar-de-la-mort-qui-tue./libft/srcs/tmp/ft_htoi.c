#include "libft.h"

int		ft_htoi(char *h)
{
	int		i;
	int		res;

	if (h == NULL)
		return (0);
	res = 0;
	while (*h && res >= 0)
	{
		i = (*h >= '0' && *h <= '9') ? *h - '0' : *h - 87;
		res = (res << 4) | i;
		h++;
	}
	return (res);
}
