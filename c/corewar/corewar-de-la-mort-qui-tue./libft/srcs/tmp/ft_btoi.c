#include "libft.h"

int		ft_btoi(char *bin)
{
	int		i;
	int		j;

	if (bin == NULL)
		return (0);
	i = 0;
	j = sizeof(int) * 8;
	while (j-- && ((*bin == '0') || (*bin == '1')))
	{
		i <<= 1;
		if (*bin == '1')
			i++;
		bin++;
	}
	return (i);
}
